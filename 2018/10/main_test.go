func TestSolve(t *testing.T) {
	g := NewGame(ReadLines("test.txt"))
	s, time := g.Solve()
	assert.Equal(t,
		`#...#..###
#...#...#.
#...#...#.
#####...#.
#...#...#.
#...#...#.
#...#...#.
#...#..###
`, s)
	assert.Equal(t, 3, time)

	g = NewGame(ReadLines("input.txt"))
	s, time = g.Solve()
	assert.Equal(t,
		`#####...######...####...#.......#####...#....#..######..######
#....#..#.......#....#..#.......#....#..##...#.......#..#.....
#....#..#.......#.......#.......#....#..##...#.......#..#.....
#....#..#.......#.......#.......#....#..#.#..#......#...#.....
#####...#####...#.......#.......#####...#.#..#.....#....#####.
#..#....#.......#.......#.......#..#....#..#.#....#.....#.....
#...#...#.......#.......#.......#...#...#..#.#...#......#.....
#...#...#.......#.......#.......#...#...#...##..#.......#.....
#....#..#.......#....#..#.......#....#..#...##..#.......#.....
#....#..######...####...######..#....#..#....#..######..######
`, s)
	assert.Equal(t, 10007, time)

	g = NewGame(ReadLines("input-ap.txt"))
	s, time = g.Solve()
	assert.Equal(t,
		`.####....####...#.......######..#.......#....#...####...######
#....#..#....#..#............#..#.......#....#..#....#..#.....
#.......#.......#............#..#.......#....#..#.......#.....
#.......#.......#...........#...#.......#....#..#.......#.....
#.......#.......#..........#....#.......######..#.......#####.
#..###..#..###..#.........#.....#.......#....#..#.......#.....
#....#..#....#..#........#......#.......#....#..#.......#.....
#....#..#....#..#.......#.......#.......#....#..#.......#.....
#...##..#...##..#.......#.......#.......#....#..#....#..#.....
.###.#...###.#..######..######..######..#....#...####...######
`, s)
	assert.Equal(t, 10144, time)
}

