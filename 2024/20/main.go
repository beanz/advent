var input []byte

func Parts(in []byte, args ...int) (int, int) {
	w := bytes.IndexByte(in, '\n')
	s := bytes.IndexByte(in, 'S')
	w1 := w + 1
	h := len(in) / w1
	limit := 100
	if w < 20 {
		limit = 20 // somewhat arbitrary for test input
	}
	get := func(x, y int) byte {
		if 0 <= x && x < w && 0 <= y && y < h {
			return in[x+y*w1]
		}
		return '#'
	}
	sx, sy := s%w1, s/w1
	todo_back := [40000]rec{}
	todo := todo_back[:0]
	todo = append(todo, rec{sx, sy, 0})
	seen := [20000]int{}
	for len(todo) > 0 {
		var cur rec
		cur, todo = todo[0], todo[1:]
		k := cur.x + cur.y*w
		if seen[k] != 0 {
			continue
		}
		seen[k] = 1 + cur.st
		for dir := 0; dir < 4; dir++ {
			nx, ny := cur.x+DX[dir], cur.y+DY[dir]
			if get(nx, ny) != '#' {
				todo = append(todo, rec{nx, ny, cur.st + 1})
			}
		}
	}
	solve := func(cheat_neighbours []rec) int {
		res := 0
		for y := 0; y < h; y++ {
			for x := 0; x < w; x++ {
				st := seen[x+y*w]
				if st == 0 {
					continue
				}
				st--
				for _, cheat := range cheat_neighbours {
					nx, ny, md := x+cheat.x, y+cheat.y, cheat.st
					if !(0 <= nx && nx < w && 0 <= ny && ny < h) {
						continue
					}
					cst := seen[nx+ny*w]
					if cst == 0 {
						continue
					}
					cst--
					diff := cst - st - md
					if diff >= limit {
						res++
					}
				}
			}
		}
		return res
	}
	return solve(cheat_neighbours2), solve(cheat_neighbours20)
}

